package com.xhsc.meituan.tabthree;

public class TabThreeInfo {

	private int iconLogo;
	private String LogoNameText;
	private String LogoContentText;
	private String LogoExplainText;
	private int iconSwitch;
	public int getIconLogo() {
		return iconLogo;
	}
	public void setIconLogo(int iconLogo) {
		this.iconLogo = iconLogo;
	}
	public String getLogoNameText() {
		return LogoNameText;
	}
	public void setLogoNameText(String logoNameText) {
		LogoNameText = logoNameText;
	}
	public String getLogoContentText() {
		return LogoContentText;
	}
	public void setLogoContentText(String logoContentText) {
		LogoContentText = logoContentText;
	}
	public String getLogoExplainText() {
		return LogoExplainText;
	}
	public void setLogoExplainText(String logoExplainText) {
		LogoExplainText = logoExplainText;
	}
	public int getIconSwitch() {
		return iconSwitch;
	}
	public void setIconSwitch(int iconSwitch) {
		this.iconSwitch = iconSwitch;
	}
	
}
